typedef wchar_t w_char;				// ワイド文字

// UNICODEの設定でないならt_charはchar型になる
#ifdef  UNICODE
#ifndef SL_T_CHAR_DEFINED
typedef w_char t_char;
#define SL_T_CHAR_DEFINED
#endif	// SL_DEFINE_H
#else 
#ifndef SL_T_CHAR_DEFINED
typedef char t_char;
#define SL_T_CHAR_DEFINED
#endif  // SL_T_CHAR_DEFINED
#endif	// UNICODE



#endif	//  SL_TYPES_H

